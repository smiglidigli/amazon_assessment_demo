import copy


class Solution(object):
    def countCharacters(self, words, chars):
        """
        :type words: List[str]
        :type chars: str
        :rtype: int
        """

        sum = 0

        for word in words:
            temp_sum = 0
            copy_chars = str(copy.deepcopy(chars))
            for letter in word:
                if any(letter in s for s in copy_chars):
                    temp_sum += 1
                    copy_chars = copy_chars.replace(letter, '', 1)
                    # copy_chars.remove(letter)
                else:
                    temp_sum = 0
                    break
            sum += temp_sum
        return sum

